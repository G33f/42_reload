int		main(int ac, char **av)
{
	int i;

	i = 1;
	while (i < ac)
	{
		while (*av[i])
		{
			ft_putchar(av[i]);
			av[i]++;
		}
		ft_purtchar("\n");
		i++;
	}
	return (0);
}
