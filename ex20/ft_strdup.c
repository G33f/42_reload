cdhar	ft_strddup(char *src)
{
	int i;

	i = 0;
	while(src[i])
	{
		i++;
	}
	char *c = (char*) malloc(i + 1);
	while(i > 0) 
	{
		c[i] = src[i];
	}
	return(c);
}
